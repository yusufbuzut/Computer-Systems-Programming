/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
/* We can use signs to check the int x is between 0x30 and 0x39 */
/* We will use complements of numbers. For complement of 0x30 if we add number that smaller than 0x30 
our result will be negative. In this way, we can understand that the int x is greater than 0x30 by looking at their signs.*/
/* For 0x39 we will first change its sign and then get its complement. In this way if we add some integer that greater than 0x39 
to the complement that we get, our result will be negative.*/

int cmplmnt = ~0x30;

/* sum of int x and complement of 0x30*/

int lowerBound= x+cmplmnt+1;

int ngtvsign = 1<<31;
int ngtvcmplmnt = ~(ngtvsign | 0x39);

/* sum of int x and negative complement of 0x39*/

int upperBound = x+ngtvcmplmnt;

/* Now we check the signs.*/

lowerBound = ngtvsign & lowerBound;
upperBound = ngtvsign & upperBound;

/* We use | operation to check and use shifting to only check signs.*/

return !((upperBound>>31) | (lowerBound>>31));
}
/* 
 * logicalNeg - implement the ! operator, using all of 
 *              the legal operators except !
 *   Examples: logicalNeg(3) = 0, logicalNeg(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int logicalNeg(int x) {

/* We will check the two's complement of an integer. The integer 0 and it's complement has same signs. So if integer is different 
than zero, integer's sign or its two's complement's sign must be 1.*/

int signComplement = (~x + 1)>>31;
int signx = x>>31;
/* Since we can't use ! sign. We check whether result is 1 with and operation. If result is 1 we should return 0.*/
/*So, we use ^ operation to return 0 when result is 1*/
int result = (signx | signComplement) & 1;
return result^1;
}
