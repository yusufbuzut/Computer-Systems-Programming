/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
int signx = x>>31;
int signy = y>>31;

/* If x positive and y is negative 1 will returned regardless of result that we found.*/

int dif = signx & !signy;

/* We sum x and complement of y. Then we shift result 31 and check its sign. If result is positive x is greater.*/

int sum = x + ~y;
sum = sum >> 31;

/* Both result will return 0 so we take the negation.*/

return !(((!(signx ^ signy)) & sum) | dif );
}
